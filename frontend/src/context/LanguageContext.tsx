const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

function isLanguage(value: string | null | undefined): value is Language {
  return value === "en" || value === "ru" || value === "az" || value === "tr";
}

function isCurrency(value: string | null | undefined): value is Currency {
  return value === "AZN" || value === "USD" || value === "EUR" || value === "RUB" || value === "TRY";
}

function setLangCookie(lang: Language) {
  document.cookie = `lang=${lang}; path=/; max-age=31536000; samesite=lax`;
  document.documentElement.lang = lang;
}

function setCurrencyCookie(currency: Currency) {
  document.cookie = `currency=${currency}; path=/; max-age=31536000; samesite=lax`;
}

